public class Bereken {
    public static int Optellen(int getal1, int getal2){
        int uitkomst = getal1 + getal2;
        return uitkomst;
    }
    public static int Aftrekken(int getal1, int getal2){
        int uitkomst = getal1 - getal2;
        return uitkomst;
    }
    public static int Delen(int getal1, int getal2){
        int uitkomst = getal1 / getal2;
        return uitkomst;
    }
    public static int Vermenigvuldigen(int getal1, int getal2){
        int uitkomst = getal1 + getal2;
        return uitkomst;
    }
}
